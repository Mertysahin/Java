  private void jButtonSILActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSILActionPerformed
    String adres = System.getProperty("user.dir") + "\\src\\sozluk.txt";
    String secilenKelime = this.jListSIL.getSelectedValue();
    System.out.println("Seçilen Kelime : " + secilenKelime);
    String kelimeler = "";    
    try {
      Scanner okuyucu = new Scanner(new File(adres));
      while(okuyucu.hasNext()) {
        String kayit = okuyucu.nextLine();
        if(!kayit.equals(secilenKelime)) {
          kelimeler += kayit + "\n";
        }
      }
      okuyucu.close();
    } catch (FileNotFoundException ex) {
      JOptionPane.showMessageDialog(this, "HATA: " + ex.toString());
    }
    System.out.println("Kelimeler : \n" + kelimeler);
    File dosya = new File(adres);
    try {
      FileWriter yazici = new FileWriter(dosya);
      BufferedWriter yaz = new BufferedWriter(yazici);
      yaz.write(kelimeler);
      //yaz.newLine();
      yaz.close();
      JOptionPane.showMessageDialog(this, "Silme Başarılı");
    } catch (IOException ex) {
      JOptionPane.showMessageDialog(this, "HATA: " + ex.toString());
    }
    listeGuncelle();
  }//GEN-LAST:event_jButtonSILActionPerformed

