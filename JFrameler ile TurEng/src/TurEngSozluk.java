  private void jButtonARAMAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonARAMAActionPerformed
    String adres = System.getProperty("user.dir") + "\\src\\sozluk.txt";
    String sonuc = "";
    String arananKelime = this.jTextFieldARAMA.getText();
    String kayit = "";
    String tur, eng;
    try {
      Scanner okuyucu = new Scanner(new File(adres));
      while(okuyucu.hasNext()) {
        kayit = okuyucu.nextLine();
        System.out.println("kayıt : " + kayit);
        if(this.jRadioButtonTUMU.isSelected()) {
          tur = kayit.substring(0, kayit.indexOf("=")-1);
          System.out.println("Türkçe : " + tur);
          eng = kayit.substring(kayit.indexOf("=")+2, kayit.length());
          System.out.println("English : " + eng);
          if(arananKelime.equalsIgnoreCase(tur)) {
            sonuc += "Aranan : " + arananKelime + " = " + eng + "\n";
          } else if(arananKelime.equalsIgnoreCase(eng)) {
            sonuc += "Searched : " + arananKelime + " = " + tur + "\n";
          }
        } else {
          // içinde geçen karakterleri bulur
          if(kayit.contains(arananKelime)) {
            sonuc += arananKelime + " -> " + kayit + "\n";
          }
        }
      }
      if(sonuc.equals("")) sonuc = "Aranan Kelime Bulunamadı";
      this.jTextAreaARAMA.setText(sonuc);
    } catch (FileNotFoundException ex) {
      JOptionPane.showMessageDialog(this, "HATA:" + ex.toString());
    }
  }//GEN-LAST:event_jButtonARAMAActionPerformed

